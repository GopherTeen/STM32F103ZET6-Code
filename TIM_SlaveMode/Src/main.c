// 定时器更新中断
// 自动重装载寄存器和从模式控制器（复位模式）都会触发更新中断，将更新中断标志位置 1
// 从模式控制器在接收到触发信号时，还会设置一个触发器中断标志位
char updateMsg[] = "自动重装载\r\n";
char triggerMsg[] = "从模式触发\r\n";
void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef* htim) {
    if (htim->Instance == TIM2) {
        if (__HAL_TIM_GET_FLAG(htim, TIM_FLAG_TRIGGER) == SET) {
            // HAL 库并未将触发器中断标志位清除，所以我们需要手动清除
            __HAL_TIM_CLEAR_FLAG(htim, TIM_FLAG_TRIGGER);
            HAL_UART_Transmit(&huart1, (uint8_t*) triggerMsg, sizeof(triggerMsg), 100);
        } else {
            HAL_UART_Transmit(&huart1, (uint8_t*) updateMsg, sizeof(updateMsg), 100);
        }
    }
}
